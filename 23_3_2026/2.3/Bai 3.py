a = int(input("Nhập a: "))
b = int(input("Nhập b: "))
c = int(input("Nhập c: "))

# a) Tổng và tích
print("Tổng =", a + b + c)
print("Tích =", a * b * c)

# b) Hiệu (lấy ví dụ a - b)
print("Hiệu a - b =", a - b)

# c) Chia (lấy ví dụ a và b)
print("Chia nguyên a // b =", a // b)
print("Chia dư a % b =", a % b)
print("Chia chính xác a / b =", a / b)